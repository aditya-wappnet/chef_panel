String? nameValidator(String? value) {
  String pattern = r'^[a-zA-Z_-]+$';
  RegExp regex = new RegExp(pattern);

  if (value!.isEmpty) {
    return 'Please this field must be filled';
  } else if (!regex.hasMatch(value)) {
    return 'Please enter on alphabets only';
  }
  return null;
}

